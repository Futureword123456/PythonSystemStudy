"""判断字符串是否为数字"""


a = (input("输入a:"))

"""判断字符串是否为数字"""
print(a.isdigit())
"""判断字符串是否为字母"""
print(a.isalpha())

"""判断字符串是否为字母和数字的组合"""
print(a.isalnum())
